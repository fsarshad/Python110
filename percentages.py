females = int( input( "Please enter the number of females in the class: "))
males = int( input( "Please enter the number of males in the class: "))
totalstudents = females + males 
femalespercentage = ( females / totalstudents ) * 100 
malespercentage = ( males / totalstudents ) * 100
print( "females percentage: " + str( femalespercentage ) + "%" )  
print( "males percentage: " +  str( malespercentage ) + "%" ) 


